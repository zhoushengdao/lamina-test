use lamina_test::s;
use rstest::rstest;

#[rstest]
#[case("[[]]", "[[]]")]
#[case("[[1]]", "[[1]]")]
#[case("[[1, 2]]", "[[1, 2]]")]
#[case("[[1, 2, 3]]", "[[1, 2, 3]]")]
#[case("[[1], [1]]", "[[1], [1]]")]
#[case("[[1, 2], [1, 2]]", "[[1, 2], [1, 2]]")]
#[case("[[1, 2, 3], [1, 2, 3]]", "[[1, 2, 3], [1, 2, 3]]")]
#[case("[[1], [1], [1]]", "[[1], [1], [1]]")]
#[case("[[1, 2], [1, 2], [1, 2]]", "[[1, 2], [1, 2], [1, 2]]")]
#[case(
    "[[1, 2, 3], [1, 2, 3], [1, 2, 3]]",
    "[[1, 2, 3], [1, 2, 3], [1, 2, 3]]"
)]
fn special(#[case] input: String, #[case] expected: String) {
    assert_eq!(expected, s(input.clone()), "简单表达式“{}”", input);
}
